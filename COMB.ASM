	;
        ;	FILENAME  : MATRIX.ASM
        ;	PROCESSOR : I8086
        ;	VER.      : V1.1
        ;
CODE	SEGMENT
	ASSUME	CS:CODE,DS:CODE,ES:CODE,SS:CODE
	;

LED: 

PPIC_C	EQU	1EH ; control register
PPIC	EQU	1CH 
PPIB	EQU	1AH
PPIA	EQU	18H
	;	
	ORG	1000H
	MOV	AL,10000000B
	OUT	PPIC_C,AL
	;
	MOV	AL,11111111B
	OUT	PPIC,AL
	;
	MOV	AL,11111111B
	OUT	PPIB,AL
	;
L1:	MOV	AL,11111110B
L2:	OUT	PPIA,AL
	CALL	TIMER
	STC
	ROL	AL,1
	JC	L2
	;JMP	L1
JMP MATRIX


		
	;
	INT	3
	;		
	;



	;


MATRIX:



PPIC_CCC	EQU	1EH ; control register
PPICCC	EQU	1CH 
PPIBBB	EQU	1AH
PPIAAA	EQU	18H





	;	
	;ORG	1000H
	MOV	AL,10000000B
	OUT	PPIC_CCC,AL
	;
	MOV	AL,11111111B
	OUT	PPICCC,AL
	;
	MOV	AL,11111111B
	OUT	PPIBBB,AL
	;
L3:	MOV	AL,11111110B
L4:	OUT	PPIAAA,AL
	CALL	TIMER
	STC
	ROL	AL,1
	JC	L4
	;JMP	L1

JMP FND		
	;
	INT	3
	;		



TIMER:	MOV	CX,0FFFFH
TIMER1:	NOP
	NOP
	NOP
	NOP
	LOOP	TIMER1
	RET
	;

FND:


PPIC_CC	EQU	1FH
PPICC	EQU	1DH
PPIBB	EQU	1BH
PPIAA	EQU	19H
	;	

	;ORG	1000H
	MOV	AL,10000000B
	OUT	PPIC_CC,AL
	;
	MOV	AL,11110000B
	OUT	PPIBB,AL
	;
	MOV	AL,00000000B
	OUT	PPICC,AL
	;
L5: 	MOV	SI,OFFSET DATA
L6:	MOV	AL,BYTE PTR CS:[SI]
	CMP	AL,00H 
	JE EXIT
	CALL	LED
	OUT	PPIAA,AL
	CALL	TIMER
	INC	SI
	JMP	L6		
	;
	INT	3
	;		

DATA:	DB	11000000B
	DB	11111001B
	DB	10100100B
	DB	10110000B
	DB	10011001B
	DB	10010010B
	DB	10000010B
	DB	11111000B
	DB	10000000B
	DB	10010000B
	DB	00H
  
  
  
  EXIT:Ret 
CODE	ENDS
	END


