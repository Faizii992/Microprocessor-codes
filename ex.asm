	;
        ;	FILENAME  : MATRIX.ASM
        ;	PROCESSOR : I8086
        ;	VER.      : V1.1
        ;
CODE	SEGMENT
	ASSUME	CS:CODE,DS:CODE,ES:CODE,SS:CODE
	;
PPIC_C	EQU	1EH ; control register
PPIC	EQU	1CH 
PPIB	EQU	1AH
PPIA	EQU	18H
	;	
	ORG	1000H
	MOV	AL,10000000B
	OUT	PPIC_C,AL
	;
	MOV	AL,11111111B
	OUT	PPIC,AL
	;
	MOV	AL,11111111B
	OUT	PPIB,AL



PPIC_CC	EQU	1FH
PPICC	EQU	1DH
PPIBB	EQU	1BH
PPIAA	EQU	19H
	;	

	;
L1:	MOV	AL,11111110B
L2:	OUT	PPIA,AL
	CALL	TIMER
	STC
	ROL	AL,1
	JC	L2

	MOV 	AL,11111111B
	OUT PPIA,AL
	CALL LED

	
	JMP	L1		
	;
	INT	3
	;		
TIMER:	MOV	CX,0FFFFH
TIMER1:	NOP
	NOP
	NOP
	NOP
	LOOP	TIMER1
	RET
	;
LED:	
	MOV	AL,10000000B
	OUT	PPIC_CC,AL
	;
	MOV	AL,11111111B
	OUT	PPIAA,AL
	;
	MOV	AL,00000000B
	OUT	PPICC,AL
	
	L3:	MOV	AL,11110001B
	L4:	OUT	PPIBB,AL
	CALL	TIMER
	SHL	AL,1
	TEST	AL,00010000B
	JNZ	SEVEN
	OR	AL,11110000B
	JMP	L4		
	;
	;

SEVEN:	
	MOV	AL,10000000B
	OUT	PPIC_CC,AL
	;
	MOV	AL,11110000B
	OUT	PPIBB,AL
	;
	MOV	AL,00000000B
	OUT	PPICC,AL
	;
L6: 	MOV	SI,OFFSET DATA
L5:	MOV	AL,BYTE PTR CS:[SI]
	CMP	AL,00H
	JE	EXIT
	OUT	PPIAA,AL
	CALL	TIMER
	INC	SI
	JMP	L5

	

EXIT: RET

DATA:	DB	11000000B
	DB	11111001B
	DB	10100100B
	DB	10110000B
	DB	10011001B
	DB	10010010B
	DB	10000010B
	DB	11111000B
	DB	10000000B
	DB	10010000B
	DB	11111111B
	DB	00H

CODE	ENDS
	END


